// Enum for different placements of hands when holding items 
public enum HoldOrientations {

	Normal, // The normal hand positions
	OneHandedCenter, // Show one hand, in the center
	OneHandedRight, // Show only the right hand 
	OneHandedLeft, // Show only the left hand
	DoubleHandedClose, // Show both hands, but closer together than usual
	
}
